function halfNumber(val)  {
    let result = val / 2
    console.log(`Half of ${val} is ${result}`)
    
}

halfNumber(18)

//STEP 2

function squareNumber(sqr)  {
    let final = sqr * sqr
    console.log(`the result of squring ${sqr} is ${final}`)
    
}

squareNumber(7)

//STEP 3

function percentOf(first, second)  {
    let fin = (first / second * 100).toFixed(2)
    console.log(`${first} is ${fin}% of ${second}`)
    
}

percentOf(17,31)

//STEP 4

function findModulus( uno, dos)  {
    let elFinal = uno % dos
    console.log(`${elFinal} is the modulus of ${uno} and ${dos}`)
    
}

findModulus(22,31)

